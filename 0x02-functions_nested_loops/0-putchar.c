#include "main.h"

/**
  * main - Entry point
  *
  * Return: Always 0 (Success)
  */
int main(void)
{
	char n = 1;

	while (n <= 1)
	{
		_putchar('_');
		_putchar('p');
		_putchar('u');
		_putchar('t');
		_putchar('c');
		_putchar('h');
		_putchar('a');
		_putchar('r');
		_putchar('\n');
		n = n + 1;	
	}

	return (0);
}
